try
    userInputInt = round(str2double(userInputStr));
    
    % 检查是否成功将字符串转换为整数
    if isnan(userInputInt)
        errordlg('无法将输入解释为整数', '错误', 'modal');
        return;
    end
        % 现在 userInputInt 包含整数值
    disp(['用户输入的整数是：' num2str(userInputInt)]);
    
    % 在这里可以使用 userInputInt 进行进一步的处理

catch
    errordlg('无法解释输入', '错误', 'modal');
end

srcImage = handles.noisyImage;
for i = 0 : userInputInt
    noisyImage2 = imnoise(handles.imageData, 'gaussian', 0, 0.1);
    srcImage = ((srcImage + noisyImage2)/2);
    %srcImage = uint8((double(handles.noisyImage) + double(handles.imageData).*userInputInt)/(userInputInt+1));
end
srcImage=uint8(srcImage);
axes(handles.axes1);
imshow(srcImage);
axes(handles.axes4);
imshow(handles.imageData-srcImage);
